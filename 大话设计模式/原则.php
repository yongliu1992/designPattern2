单一职责原则
对一个类而言，有且仅有一个引起它变化的原因ASD
将不同的逻辑进行分离，比如哪些是界面，哪些是逻辑
软件设计的真正要做的许多内容， 就是发现职责，并把职责相互分离，
如果我们能想到多余一个的动机去改变一个类，那么这个类就具有多余一个类的职责。
开放封闭原则，2大特征，1对于扩展式开放的，对于更改是封闭的
开放封闭原则是面向对象设计的核心所在，遵循这个原则可以带来面向对象技术声称的巨大好处，也就是
可维护、可扩展、可复用、灵活性好。开发人员将经常变化的部分做出抽象，然而对于应用程序
的每个部分刻意地做出抽象同样不是一个好主意.拒绝不成熟的抽象和抽象本身一样重要。
